/* 
    snake_case for table and column names, not camelCase. postgres ist nicht case-sensitive
*/

CREATE TABLE products (
    product_id TEXT PRIMARY KEY,
    name VARCHAR(50) NOT NULL,
    category VARCHAR(50) NOT NULL,
    description TEXT,
    price INTEGER NOT NULL CHECK (price >= 0),
    shipping_cost INTEGER NOT NULL CHECK (shipping_cost >= 0),
    brand VARCHAR(25) NOT NULL,
    stock_status BOOLEAN NOT NULL DEFAULT FALSE,
    shipping_time INTEGER NOT NULL CHECK (shipping_time > 0),
    active BOOLEAN NOT NULL DEFAULT FALSE
);
CREATE UNIQUE INDEX idx_product_name ON products (name);
/* 
    Index ist für schnellere Suche nach Produkten im Frontend / in Searchbar  
*/

CREATE TABLE images (
    image_id TEXT PRIMARY KEY,
    url TEXT,
    product_id TEXT REFERENCES products(product_id) ON DELETE CASCADE
);

CREATE TABLE users (
    user_id TEXT PRIMARY KEY,
    email VARCHAR(100) NOT NULL UNIQUE,
    password TEXT NOT NULL,
    phone INTEGER,
    address VARCHAR(200),
    postcode INTEGER,
    first_name VARCHAR(50),
    last_name VARCHAR(50),
    role VARCHAR(20) NOT NULL,
    active BOOLEAN NOT NULL DEFAULT TRUE,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE orders (
    order_id TEXT PRIMARY KEY,
    user_id TEXT REFERENCES users(user_id) ON DELETE SET NULL,
    order_date TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    total_amount INTEGER NOT NULL CHECK (total_amount >= 0),
    delivery_fullname VARCHAR(50),
    delivery_address VARCHAR(200),
    delivery_postcode INTEGER,
    payment_method VARCHAR(20) NOT NULL,
    order_status VARCHAR(20) NOT NULL,
    delivery_status VARCHAR(20) NULL
);

CREATE TABLE orders_products (
    order_id TEXT NOT NULL REFERENCES orders(order_id) ON DELETE CASCADE,
    product_id TEXT REFERENCES products(product_id) ON DELETE SET NULL,
    amount INTEGER NOT NULL CHECK (amount > 0),
    PRIMARY KEY (order_id, product_id)
);

CREATE TABLE reviews (
    review_id TEXT PRIMARY KEY,
    product_id TEXT NOT NULL REFERENCES products(product_id) ON DELETE CASCADE,
    user_id TEXT REFERENCES users(user_id) ON DELETE SET NULL,
    rating INTEGER NOT NULL,
    comment TEXT,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE messages (
    message_id TEXT PRIMARY KEY,
    name VARCHAR(50) NOT NULL,
    email VARCHAR(100) NOT NULL,
    subject VARCHAR(100) NULL,
    message VARCHAR(500) NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);


